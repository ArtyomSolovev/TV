
import Foundation

// MARK: - Welcome
struct Welcome: Codable {
    let channels: [Channel]
    let categories: [Category]
    let genres: [Genre]
    let paidPacks: [JSONAny]
    let valid: Int
    let source, cacheKey: String
    let country: Int

    enum CodingKeys: String, CodingKey {
        case channels, categories, genres
        case paidPacks = "paid_packs"
        case valid, source, cacheKey, country
    }
}

// MARK: - Category
struct Category: Codable {
    let id: Int
    let identifier, nameRu, nameEn: String
    let sort: Int

    enum CodingKeys: String, CodingKey {
        case id, identifier
        case nameRu = "name_ru"
        case nameEn = "name_en"
        case sort
    }
}

// MARK: - Channel
struct Channel: Codable {
    let id: Int
    let epgID: Int?
    let nameRu, nameEn: String
    let cdn, url: String
    let address: String
    let urlArchive: String
    let urlSound: String
    let pack: [Int]
    let channelPublic: Int
    let image: String
    let aspectRatio, channelAspectRatio: AspectRatio
    let fav, hasEpg: Bool
    let current: Current?
    let withArchive: Bool
    let regionCode, tz: Int
    let promo: Bool
    let categories: [Int]
    let sort: Int
    let genre: [Int]
    let dayArchive: Int?
    let quality: [Int]
    let number, useExternalPlayer: Int
    let externalPlayerCode: String?
    let isForeign: Bool
    let foreignPlayer: ForeignPlayer
    let foreignPlayerKey: Bool
    let href: String

    enum CodingKeys: String, CodingKey {
        case id
        case epgID = "epg_id"
        case nameRu = "name_ru"
        case nameEn = "name_en"
        case cdn, url, address
        case urlArchive = "url_archive"
        case urlSound = "url_sound"
        case pack
        case channelPublic = "public"
        case image, aspectRatio
        case channelAspectRatio = "aspect_ratio"
        case fav, hasEpg, current
        case withArchive = "with_archive"
        case regionCode = "region_code"
        case tz, promo, categories, sort, genre
        case dayArchive = "day_archive"
        case quality, number
        case useExternalPlayer = "use_external_player"
        case externalPlayerCode = "external_player_code"
        case isForeign = "is_foreign"
        case foreignPlayer = "foreign_player"
        case foreignPlayerKey = "foreign_player_key"
        case href
    }
}

enum AspectRatio: String, Codable {
    case the169 = "16/9"
    case the43 = "4/3"
}

// MARK: - Current
struct Current: Codable {
    let timestart, timestop: Cdnvideo
    let title, desc: String
    let cdnvideo, rating: Cdnvideo
}

enum Cdnvideo: Codable {
    case integer(Int)
    case string(String)

    init(from decoder: Decoder) throws {
        let container = try decoder.singleValueContainer()
        if let x = try? container.decode(Int.self) {
            self = .integer(x)
            return
        }
        if let x = try? container.decode(String.self) {
            self = .string(x)
            return
        }
        throw DecodingError.typeMismatch(Cdnvideo.self, DecodingError.Context(codingPath: decoder.codingPath, debugDescription: "Wrong type for Cdnvideo"))
    }

    func encode(to encoder: Encoder) throws {
        var container = encoder.singleValueContainer()
        switch self {
        case .integer(let x):
            try container.encode(x)
        case .string(let x):
            try container.encode(x)
        }
    }
}

